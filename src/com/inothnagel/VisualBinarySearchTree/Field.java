package com.inothnagel.VisualBinarySearchTree;

public class Field {
    private float width = 1000;
    private float height = 1000;

    public Field(float width, float height) {
        this.width = width;
        this.height = height;
    }

    public float getWidth() {
        return width;
    }

    public float getHeight() {
        return height;
    }
}
